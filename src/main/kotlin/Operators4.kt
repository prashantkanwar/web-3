fun main() {
    var a = 5
    print(" add the value of a and 2 = ")
    a += 2 // a = a + 2
    println(a) // 7
    print("subtract 4 from the value of a = ")
    a -= 4 // a = a - 4
    println(a) // 3
    print("multiply by 5 to the value of a = ")
    a *= 5
    println(a) // 15
    print("divide the value of a by 5")
    a /= 5
    println(a) // 3 (beware integer division)
    print("a mode 2 = ")
    a %= 2
    println(a) // 1
}
